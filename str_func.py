# Создаём функцию, которая принимает на вход строку и возвращает ее
# со всеми заглавными буквами.
def get_capitalize(text):
    return text.upper()

# Создаём функцию, которая делает заглавными первые буквы каждого слова
# в строке, поступившей на вход функции.
def get_title(text):
    return text.capitalize()
